type Copy = Record<keyof typeof copy['zh-CN'], string>

const LocaleContext = createContext<{ locale: Locale; setLocale: (locale: Locale) => void; t: Copy } | null>(null)

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => readStoredValue<Locale>('utility-hub-locale') ?? 'zh-CN')

  const value = useMemo(
    () => ({
      locale,
      setLocale: (nextLocale: Locale) => {
        setLocaleState(nextLocale)
        writeStoredValue('utility-hub-locale', nextLocale)
        document.documentElement.lang = nextLocale
      },
      t: copy[locale] as Copy,
    }),
    [locale],
  )

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>
}

export function useLocale() {
  const context = useContext(LocaleContext)
  if (!context) throw new Error('useLocale must be used within LocaleProvider')
  return context
}
